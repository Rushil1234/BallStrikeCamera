// St Andrews DEM: EU-DEM 25m via OpenTopoData (USGS EPQS/3DEP are US-only).
// Links relief is subtle (dunes, the Swilcan valley) but real.
import fs from 'fs/promises';

const ORIGIN = { lat: 56.34750, lng: -2.81020 };
const BBOX = { south: 56.333, west: -2.834, north: 56.363, east: -2.784 };
const WIDTH = 100;
const HEIGHT = 80;
const OUT_PATH = new URL('../js/standrews-elevation.js', import.meta.url);
const CACHE_PATH = '/tmp/standrews-eudem-cache.json';

function project(lat, lng) {
  return {
    x: (lng - ORIGIN.lng) * Math.cos(ORIGIN.lat * Math.PI / 180) * 111320,
    z: (lat - ORIGIN.lat) * 111320,
  };
}
function percentile(values, p) {
  const sorted = [...values].sort((a, b) => a - b);
  return sorted[Math.max(0, Math.min(sorted.length - 1, Math.floor((sorted.length - 1) * p)))];
}

const pts = [];
for (let y = 0; y < HEIGHT; y++) {
  const lat = BBOX.south + (BBOX.north - BBOX.south) * (y / (HEIGHT - 1));
  for (let x = 0; x < WIDTH; x++) {
    const lng = BBOX.west + (BBOX.east - BBOX.west) * (x / (WIDTH - 1));
    pts.push([lat, lng]);
  }
}
let cache = {};
try { cache = JSON.parse(await fs.readFile(CACHE_PATH, 'utf8')); } catch {}

const values = new Array(pts.length).fill(null);
const missing = [];
for (let i = 0; i < pts.length; i++) {
  const key = `${pts[i][0].toFixed(6)},${pts[i][1].toFixed(6)}`;
  if (Object.hasOwn(cache, key)) values[i] = cache[key];
  else missing.push(i);
}
console.log(`grid ${WIDTH}x${HEIGHT}=${pts.length}, fetching ${missing.length}`);

for (let b = 0; b < missing.length; b += 100) {
  const idxs = missing.slice(b, b + 100);
  const locs = idxs.map((i) => `${pts[i][0]},${pts[i][1]}`).join('|');
  let ok = false;
  for (let attempt = 1; attempt <= 4 && !ok; attempt++) {
    try {
      const res = await fetch(`https://api.opentopodata.org/v1/eudem25m?locations=${locs}`);
      if (res.status === 429) throw new Error('rate limited');
      const data = await res.json();
      if (data.status !== 'OK') throw new Error(data.error || 'bad response');
      data.results.forEach((r, j) => {
        const v = Number(r.elevation);
        const val = Number.isFinite(v) ? v : null;
        values[idxs[j]] = val;
        cache[`${pts[idxs[j]][0].toFixed(6)},${pts[idxs[j]][1].toFixed(6)}`] = val;
      });
      ok = true;
    } catch (e) {
      console.warn(`batch ${b / 100}: ${e.message}, retry ${attempt}`);
      await new Promise((r) => setTimeout(r, 2500 * attempt));
    }
  }
  if (!ok) throw new Error('EU-DEM fetch failed');
  process.stdout.write('.');
  await new Promise((r) => setTimeout(r, 1100));   // 1 req/s API limit
}
await fs.writeFile(CACHE_PATH, JSON.stringify(cache), 'utf8');
process.stdout.write('\n');

for (let pass = 0; pass < 5; pass++) {
  let changed = false;
  for (let y = 0; y < HEIGHT; y++) for (let x = 0; x < WIDTH; x++) {
    const i = y * WIDTH + x;
    if (Number.isFinite(values[i])) continue;
    let sum = 0, n = 0;
    for (const [dx, dy] of [[1,0],[-1,0],[0,1],[0,-1]]) {
      const nx = x + dx, ny = y + dy;
      if (nx < 0 || nx >= WIDTH || ny < 0 || ny >= HEIGHT) continue;
      const v = values[ny * WIDTH + nx];
      if (Number.isFinite(v)) { sum += v; n++; }
    }
    if (n) { values[i] = sum / n; changed = true; }
  }
  if (!changed) break;
}
const finite = values.filter(Number.isFinite);
const fb = percentile(finite, 0.25) || 0;
const rounded = values.map((v) => Number((Number.isFinite(v) ? v : fb).toFixed(2)));
const sw = project(BBOX.south, BBOX.west);
const ne = project(BBOX.north, BBOX.east);
const data = {
  attribution: 'EU-DEM 25m via OpenTopoData',
  source: 'https://www.opentopodata.org/datasets/eudem/',
  origin: ORIGIN, bbox: BBOX, width: WIDTH, height: HEIGHT,
  worldBounds: {
    minX: Number(sw.x.toFixed(2)), maxX: Number(ne.x.toFixed(2)),
    minZ: Number(sw.z.toFixed(2)), maxZ: Number(ne.z.toFixed(2)),
  },
  min: Number(Math.min(...finite).toFixed(2)),
  max: Number(Math.max(...finite).toFixed(2)),
  base: Number(percentile(finite, 0.04).toFixed(2)),
  scale: 1,
  values: rounded,
};
await fs.writeFile(OUT_PATH,
  `// Generated by tools/generate-standrews-elevation.mjs (EU-DEM 25m).\n\nexport const STANDREWS_ELEVATION = ${JSON.stringify(data)};\n`);
console.log(`wrote standrews-elevation.js (${WIDTH}x${HEIGHT}, ${data.min}m..${data.max}m)`);
